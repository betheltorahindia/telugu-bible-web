/**
 * Helpers
 * -------
 * aliyahId(b,c,v) creates a stable id like "b1-c2-v4".
 * aliyahStartSet contains every verse id that is an aliyah START.
 * The UI uses this to:
 *   - mark aliyah starts with a small badge on chapter pages
 *   - create links that jump directly to the correct verse anchor (#v{n})
 */
export const aliyahId = (b: number, c: number, v: number) => `b${b}-c${c}-v${v}`;

export const aliyahStartSet = new Set(
  PARASHIYOT.flatMap((p) => p.aliyot.map((a) => aliyahId(a.b, a.c, a.v)))
);

/**
 * Optional utility: get a specific aliyah start reference.
 * (Not required, but handy if you ever need it in code.)
 */
export function getAliyahStart(parashaName: string, aliyahIndex1to7: number) {
  const p = PARASHIYOT.find((x) => x.name === parashaName);
  if (!p) return undefined;
  const a = p.aliyot[aliyahIndex1to7 - 1];
  return a ? { ...a } : undefined;
}
